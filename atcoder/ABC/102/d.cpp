const int oo = 1e18;

auto main() -> signed {
  fastio;

  int n;
  in(n);
  V<int> v(n);
  in(v);

  for(int i = 1; i < n; i++)
    v[i] += v[i-1];

  auto find = [&](int l, int r) {
    int sum = v[r] - (l ? v[l-1] : 0);
    auto lb = lower_bound(begin(v), end(v), sum/2 + (l ? v[l-1] : 0));
    auto cp = lb;
    if(lb-begin(v) != l) lb--;
    int secsum = *lb - (l ? v[l-1] : 0);

    int tenta2 = *cp - (l ? v[l-1] : 0);

    /*
    db(var(*lb), var(*cp));
    db(var(tenta2), var(secsum));
    */

    if(abs(sum - 2*tenta2) < abs(sum - 2*secsum))
      return ii{tenta2, sum - tenta2};

    return ii{secsum, sum - secsum}; 
  };

  int ans = oo;

  for(int i = 1; i < n-2; i++) {
    auto [a, b] = find(0, i);
    auto [c, d] = find(i+1, n-1);
    /*
    cout << "-------------- db ----------------\n";
    db(var(v[i]), var(v.back() - v[i]));
    db(var(i), var(a), var(b), var(c), var(d));
    */

    ans = min(ans, max({a,b,c,d}) - min({a, b, c, d}));
  }
  out(ans);
}
