  fastio;

  vector<vector<int>> ans = {{1, 1}, {1, 0}};
  vector<vector<int>> base = {{1}, {0}};

  int n; cin >> n;
  if(n < 2) return cout << n << endl, 0;
  ans = fexp(ans, n-1);

  ans = mul(ans, base);

